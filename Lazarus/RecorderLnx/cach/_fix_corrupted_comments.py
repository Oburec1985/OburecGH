def nl(text: str) -> str:
    return text.replace("\r\n", "\n").replace("\r", "\n")


def to_crlf(text: str) -> bytes:
    return nl(text).replace("\n", "\r\n").encode("utf-8")


def read_utf8(path: Path) -> str:
    return nl(path.read_text(encoding="utf-8"))


def write_utf8(path: Path, text: str) -> None:
    path.write_bytes(to_crlf(text))


def count_qmarks(text: str) -> int:
    return len(re.findall(r"\?{3,}", text))


def scan_corrupted() -> list[tuple[Path, int]]:
    hits: list[tuple[Path, int]] = []
    for root in SCAN_ROOTS:
        if not root.is_dir():
            continue
        for path in sorted(root.rglob("*.pas")):
            if path.parent.name == "cach":
                continue
            try:
                text = read_utf8(path)
            except (UnicodeDecodeError, OSError):
                continue
            n = count_qmarks(text)
            if n:
                hits.append((path, n))
    return hits


